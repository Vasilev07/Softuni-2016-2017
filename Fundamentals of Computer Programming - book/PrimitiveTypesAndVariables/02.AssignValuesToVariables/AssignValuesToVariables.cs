using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _02.AssignValuesToVariables
{
    class AssignValuesToVariables
    {
        static void Main(string[] args)
        {
            //5
            double firstNum = 5;
            Console.WriteLine(firstNum);
            //-5.01
            double secondNum = -5.01F;
            Console.WriteLine(secondNum);
            //34.567839023
            double thirdNum = 34.567839023;
            Console.WriteLine(thirdNum);
            //12.345
            float fourthNum = 12.345F;
            Console.WriteLine(fourthNum);
            //8923.1234857
            decimal fifthNum = 8923.1234857M;
            Console.WriteLine(fifthNum);
            //3456.091124875956542151256683467
            decimal sixthNum = 3456.091124875956542151256683467M;
            Console.WriteLine(sixthNum);

        }
    }
}
