using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _05.ComparisonOperators
{
    class ComparisonOperators
    {
        static void Main(string[] args)
        {
            int x = 10;
            int y = 5;
            Console.WriteLine(x > y); //true
            Console.WriteLine(x < y);//false
            Console.WriteLine(x >= y);//true
            Console.WriteLine(x <= y);//false
            Console.WriteLine(x == y);//false
            Console.WriteLine(x != y);//true
        }
    }
}
