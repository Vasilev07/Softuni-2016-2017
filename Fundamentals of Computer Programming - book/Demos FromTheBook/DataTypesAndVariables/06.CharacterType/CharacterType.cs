using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _06.CharacterType
{
    class CharacterType
    {
        static void Main(string[] args)
        {
            char pesho = 'a';
            Console.WriteLine("The code of this char: " + pesho + " is: " + (int)pesho);

            char gosho = 'b';
            Console.WriteLine("The code of this char: " + gosho + " is: " + (int)gosho);

            char ivan = 'A';
            Console.WriteLine("The code of this char: " + ivan + " is: " + (int)ivan);
        }
    }
}
